package functions

// addition this is func for add 
func Addition(a int, b int) int {
	var c int
	c = a + b
	return c
}
// multiple this is func for Multiple 

func Multiple (a int, b int) int {
	var c int
	c = a * b 
	return c 
}

func Subtraction (a int, b int) int {
	var c int
	c = a - b 
	return c 
}

func Division (a float64 , b float64) float64 {
	var c float64
	c = a / b
	return c
}
